    private boolean errorMessageOnEmpty(String value, String message) {
        if (value != null ) {
            if (value.trim().length() ==  0 ) {
                NotifyBox.error(message);
                return false;
            }
        } else {
            NotifyBox.error(message);
            return false;
        }
        return true;
    }
        
