    private void init(ArrayList<String> xmlText) throws ParserConfigurationException{
        setupTree();
        setupTextArea(xmlText);
    }



    private void setupTextArea(ArrayList<String> xmlText) {
        xmlSource.setText((String) xmlText.get(0) + "\n");
        for (int i = 1; i < xmlText.size(); i++) {
            xmlSource.append((String) xmlText.get(i) + "\n");
        }
        bungeniXmlTree.refresh(xmlSource.getText());
    }

    private void setupTree() throws ParserConfigurationException{
         // Next, create the BungeniXmlTree
        bungeniXmlTree = new BungeniXmlTree();
        bungeniXmlTree.getSelectionModel().setSelectionMode(TreeSelectionModel.SINGLE_TREE_SELECTION);
        bungeniXmlTree.setShowsRootHandles(true);

        // A more advanced version of this tool would allow the JTree to be editable
        bungeniXmlTree.setEditable(false);

        // Wrap the JTree in a JScroll so that we can scroll it in the JSplitPane.
        scrollXmlTree.getViewport().add(bungeniXmlTree);

    }

    public void updateComponent (ArrayList<String> xmlText) {
        this.setupTextArea(xmlText);
    }


